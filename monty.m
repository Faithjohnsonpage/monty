pint     # Print the top element of the stack
swap     # Swap the top two elements of the stack
pop      # Pop the top element of the stack
add      # Add the top two elements of the stack
nop      # No operation (should do nothing)
sub      # Subtract the top element from the second top element
div      # Divide the second top element by the top element
mul      # Multiply the top two elements of the stack
mod      # Compute the remainder of the division of the second top element by the top element
